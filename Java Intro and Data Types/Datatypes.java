

public class Datatypes {
    public static void main(String[] args) {
        // Primitive Datatypes{ by using "//" u will be able to make comments for code explanation}
        // 1.Numbers
        byte num1=6;
        int num2=3;
        short num3=4;
        long num4=10000000000L;
        
        //decimals
        float num5=5.5f;
        double num6=5.5;

        //boolean
        boolean num7=false;//default value is false 0

        // Non- primitive Data types
        // 1.String
        String name="YRDAMAN";

        //other user Defined 
        System.out.println("Byte = "+num1);
        System.out.println("Integer = "+num2);
        System.out.println("Short = "+num3);
        System.out.println("Long = "+num4);
        System.out.println("Float = "+num5);
        System.out.println("Double = "+num6);
        System.out.println("Boolean = "+num7);
        System.out.println("String = "+name);

    }
}
