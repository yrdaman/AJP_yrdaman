
public class Assignment {
   public static void main(String[] args) {
    int num1=60;
    System.out.println(num1);
    num1+=5; //num1=num1+5 Addition Assignment
    System.out.println(num1);
    
    num1-=5; //num1=num1+5 Subtraction Assignment
    System.out.println(num1);

    num1/=5; //num1=num1+5 Divison quotient Assignment
    System.out.println(num1);

    num1*=5; //num1=num1+5 Multiplication Assignment
    System.out.println(num1);

    num1%=5; //num1=num1+5 Remainder Assignment
    System.out.println(num1);
   } 
}
