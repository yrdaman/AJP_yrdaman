public class Arthimetic {
    public static void main(String[] args) {
        //Arthimetic Operators in java +,-,*,/,%

        int value1 = 60;
        int value2 = 40;
        System.out.println("Sum = "+(value1+value2));
        System.out.println("Sub = "+(value1-value2));
        System.out.println("Mul = "+(value1*value2));
        System.out.println("Quotient = "+(value1/value2));
        System.out.println("Remainder = "+(value1%value2));

        // using double variables
        double value3 = 60;
        double value4 = 40;

        System.out.println("Sum = "+(value3+value4));
        System.out.println("Sub = "+(value3-value4));
        System.out.println("Mul = "+(value3*value4));
        System.out.println("Quotient = "+(value3/value4));
        System.out.println("Remainder = "+(value3%value4));
    }
}
