
public class Relational {
    public static void main(String[] args) {
        int num1=30;
        int num2=60;
        System.out.println(num1==num2);// if the based on condition gives true if correct or false if incorrect
        System.out.println(num1>num2);
        System.out.println(num1<num2);
        System.out.println(num1<=num2);
        System.out.println(num1>=num2);
        System.out.println(num1!=num2);
    }
}
